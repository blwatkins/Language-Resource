addition = 4 + 5
puts "4 + 5 = #{addition}" # string interpolation (only works with double quotations)

subtraction = 4 - 5
puts "4 - 5 = #{subtraction}"

multiplication = 4 * 5
puts "4 * 5 = #{multiplication}"

division = 4 / 5 # integer division
puts "4 / 5 = #{division}"

division = 4.0 / 5.0 # floating point division
puts "4.0 / 5.0 = #{division}"

exponentation = 4 ** 5
puts "4 ** 5 = #{exponentation}"

modulus = 4 % 5
puts "4 % 5 = #{modulus}"