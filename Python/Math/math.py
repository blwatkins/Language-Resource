# !/usr/bin/env pyhton
# Arithmetic Operators

a = 5 + 7 # addition
print("5 + 7 = " + str(a))

b = 6 - 9 # subtraction
print("6 - 9 = " + str(b))

c = 3 * 4 # multiplication
print("3 * 4 = " + str(c))

d = 10 // 3 # integer division
print("10 // 3 = " + str(d))

e = 10 / 3 # floating point division
print("10 / 3 = " + str(e))

f = 10 % 3 # modulus
print("10 % 3 = " + str(f))

g = 5 ** 4 # exponentiation
print("5 ** 4 = " + str(g))

# augmented assignment
x = 0
x += 1

y = 10
y -= 4

r = 6
r *= 5

w = 50
w /= 2